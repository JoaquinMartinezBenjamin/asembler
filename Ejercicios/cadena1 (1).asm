PILA SEGMENT STACK
DB 32 DUP('STACK___')
PILA ENDS

DATA SEGMENT
CONTADOR DB (?),'$'
CADENA DB 'CASA BLANCA'
DATA ENDS

CODE SEGMENT 'CODE'
ASSUME CS:CODE,DS:DATA,SS:PILA
INICIO PROC FAR

PUSH DS
PUSH 0
MOV AX,DATA
MOV DS,AX
MOV CONTADOR,0

LEA SI,CADENA

COMPARAR:
MOV AL,[SI]

CMP AL,'$'
JE MOSTRAR

CMP AL,'a'
JE CONTAR_MAS
CMP AL,'A'
JE CONTAR_MAS

INC SI
JMP COMPARAR

CONTAR_MAS:
INC CONTADOR
INC SI
JMP COMPARAR

MOSTRAR:
ADD CONTADOR,30H
MOV AH,09H                ;IMPRIMIMOS 
MOV DX,OFFSET CONTADOR    ;EL RESULTADO
INT 21H 
RET

INICIO ENDP
CODE ENDS

END INICIO
